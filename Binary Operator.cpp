//Demostate the Binary Operator
#include<iostream>

using namespace std;

int main ()
{
    int a = 20, b = 10; 
    // Aditional Operator
    cout<<"a + b ="<<" "<<(a+b)<<endl;

    // Subtraction Operator
    cout<<"a - b ="<<" "<<(a-b)<<endl;

    // Multiplication Operator
    cout<<"a * b = "<<" "<<(a*b)<<endl;

    // Division Operator
    cout<<"a / b ="<<" "<<(a/b)<<endl;

    // Modulus Operator
    cout<<"a % b ="<<" "<<(a%b)<<endl;

    return 0; 

}